package com.example.smarthunter.model;

public class favoriteEvent {

    public String jenis_fav;
    public String kategori_fav;
    public String judul_fav;
    public String event_fav;
    public Integer poster_fav;

    public favoriteEvent(String jenis_fav, String kategori_fav, String judul_fav, String event_fav, Integer poster_fav) {
        this.jenis_fav = jenis_fav;
        this.kategori_fav = kategori_fav;
        this.judul_fav = judul_fav;
        this.event_fav = event_fav;
        this.poster_fav = poster_fav;
    }




}
